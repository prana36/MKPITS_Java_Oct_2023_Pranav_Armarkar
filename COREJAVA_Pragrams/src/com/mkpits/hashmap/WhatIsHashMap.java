/*
- Map Interface is present in java.util package represents a mapping between a key and a value.
- Java Map interface is not a sub type of the Collection interface.
- A map contains unique keys.
- Maps are perfect to use for key-value association mapping such as dictionaries.
- The maps are used to perform lookups by keys or when someone wants to retrieve and update elements by keys.
- Order	No (elements are not ordered)

- Fast lookup: HashMaps excel at retrieving elements based on their keys.
  On average, lookup takes constant time (O(1)) thanks to the hashing algorithm.
  This makes them ideal for scenarios where you need to frequently access data using specific keys.
  
- Efficient data association: HashMaps store data as key-value pairs, making it easy to associate related information.
  This can be helpful for tasks like storing user profiles, managing web site sessions,
  or implementing caching mechanisms.

- Simple API: HashMaps offer a straightforward API with methods for adding, removing, and
  retrieving elements based on their keys.
  This makes them easy to learn and use even for beginner programmers.
  
- HashMaps rely on a hashing algorithm to map keys to bucket locations within the hash table.
  Choosing a good hashing algorithm is crucial for ensuring efficient performance and minimizing
  collisions (when different keys map to the same bucket).
  
- Load factor: The load factor determines how full the hash table can become before resizing.
  A well-chosen load factor balances lookup performance with memory usage.
  
- Collisions: Collisions occur when different keys map to the same bucket in the hash table.
  HashMaps use collision resolution techniques like chaining to handle such situations.
  

*/
		
