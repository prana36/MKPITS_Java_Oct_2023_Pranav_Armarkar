// JavaScript Exponentiation Assignment Operator in JavaScript is represented by “**=”. 
// This operator is used to raise the value of the variable to the power of the operand which is right. 
// This can also be explained as the first variable is the power of the second operand. 
// The exponentiation operator is equal to Math.pow().

// Syntax:

// a **= b
// or
// a = a ** b

let x = 6;
console.log((x **= 2));
console.log((x **= 0));
console.log((x **= "bar"));

 x = NaN;
console.log((x **= 2));
