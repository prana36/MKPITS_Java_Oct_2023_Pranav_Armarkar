// JavaScript Addition assignment operator(+=) adds a value to a variable, The Addition Assignment (+ =) Sums up left
// and right operand values and then assigns the result to the left operand. 
//The two major operations that can be performed using this operator are the addition of numbers and the concatenation of strings.

// Syntax:

// a += b 
//concatenation of two number 
let x = 2; 
let y = 5; 
console.log(x += y); 
  
//concatenation of two string 
let a = 'Pranav'
let b = ' Arvind Armarkar'
console.log(a += b);