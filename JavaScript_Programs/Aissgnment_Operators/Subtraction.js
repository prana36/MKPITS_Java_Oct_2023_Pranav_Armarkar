// The Subtraction Assignment Operator( -=) is used to subtract a value from a variable. 
// This operator subtracts the value of the right operand from a variable and 
// assigns the result to the variable, in other words, allows 
// us to decrease the left variable from the right value.

// Syntax:

// a -= b 

let number = 9;
number -= 5;
console.log(number);