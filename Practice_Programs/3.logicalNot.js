//JavaScript NOT Operator can be used to find the flipped value of a boolean.
console.log(!true);
console.log(!false);
console.log(!"1");
console.log(!"");
console.log(!null);

console.log("====================");
console.log(!!true);
console.log(!!false);
console.log(!!"1");
console.log(!!"");
console.log(!!null);
console.log("===============");
//When we applied the NOT operator two times we got the original boolean value back.
