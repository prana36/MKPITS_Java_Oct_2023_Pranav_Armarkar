// JavaScript Logical And(&&) Operator or Logical Conjunction Operator operates on a set of operands and returns true only if all the operands are true otherwise returns false.
console.log(true && false);
console.log(true && true);
console.log(1 && 0);
console.log(1 && 2);
console.log("1" && true);
console.log("0" && true);
