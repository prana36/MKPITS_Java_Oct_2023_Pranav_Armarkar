package com.mkpits.variableScope;

public class StaticVariable {
	static long pranavSalary=100000l;
	
	public static void main(String[] args) {
		//Static variable
		//If variable is declared using static keyword just below class declaration above any method is called static variable.
		//Static variable always declear before methode and after class
		//it only access to call in methode only
	System.out.println("My Salary is :- " +pranavSalary);	
	}
    

}
